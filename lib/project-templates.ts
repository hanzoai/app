// Helper to detect what type of project to generate based on prompt
export function detectProjectType(prompt: string): 'next' | 'vite' | 'html' {
  const lowerPrompt = prompt.toLowerCase();

  if (lowerPrompt.includes('next') || lowerPrompt.includes('server') ||
      lowerPrompt.includes('api') || lowerPrompt.includes('full-stack')) {
    return 'next';
  }

  if (lowerPrompt.includes('vite') || lowerPrompt.includes('spa') ||
      lowerPrompt.includes('single page')) {
    return 'vite';
  }

  // Default to HTML for simple projects
  return 'html';
}